  on most systems based on integrated circuits can be approximated within one 
  or two months, or even more accurately using more commonly available year and 
  week number date codes which are carried on pretty much any sizable IC made 
  during or after the 1970s, maybe even before. Remember, however, that the ordering 
  and presentation can and does still differ between various vendors, and in some 
  cases it may even be obscured within a larger serial number.</p>
