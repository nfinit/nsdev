<p>While the overall ranges are mostly static from year to year and the above 
  table can be used to establish a ballpark estimate, their exact constrants can 
  still vary depending on things such as the day the month began on, leap years 
  or other factors, thus it's recommended to use an online week calendar or other 
  resource to get an exact calendar corresponding to a specific year.</p>
<h3>Four-digit codes</h3>
<p>Four-digit year/week codes are easily the most common among the majority of 
  component vendors, often appearing by themselves on individual chips and sometimes 
  entire assemblies. By observing the range of these codes, one can pin down a 
  rough DoM for practically any system (so long as it was assembled from entirely 
  new parts in a reasonable timeframe after they were recieved), or at get least 
  enough information to add reasonable context. </p>
<p>On top of the reasonable resolution offered by this method, it's also easy 
  to recognize quickly, shared among most ICs in an assembly and displaying a 
  noticable pattern that can be referenced with other chips; if you aren't sure 
  if what you're looking at is a date code, you can check if other chips share 
  similar four-digit markings.</p>
<p>These date codes vary in their format from manufacturer to manufacturer, appearing 
  in either year/week order (e.g. <b><font color="red">84</font><font color="green">05</font></b> 
  for <b><font color="green">week 5</font></b> 
  of <b><font color="red">1984</font></b>) or 
  week/year order (presented as <b><font color="green">05</font><font color="red">84</font></b> 
  for the same date) mostly equally as common.</p>
<h3>Embedded and special code formats</h3>
<p>Some vendors date their products with markings that are noticably different 
  from the more common four-digit format, although they are still fundamentally 
  similar.</p>
<p>Intel is one of the most notable examples of this kind of practice, in most 
  of their microprocessors and chipsets mader after around the mid-1980s employing 
  a monolithic serial number that conveys the year and week of manufacture as 
  well as other information such as the plant of origin. The year/week-formatted 
  date section of an Intel serial number differs from the above format in that 
  it omits the decade of manufacture from the year, so, as an example, a chip 
  manufactured in <b><font color="green">week 28</font></b> of <font color="red"><b>1995</b></font> 
  would carry the code <b></b> <font color="red"><b>5</b></font><b><font color="green">28</font></b>, 
  rather than <b><font color="red">95</font><font color="green">28</font></b>. 
  Luckily, this consideration does not provide too much of an obstacle for dating 
  a chip, as changes in packaging and branding can make the decade easily discernible.</p>
<hr>
<h2 align="center">Styles, ideas and specifications</h2>
<p align="center">The direct dating methods above often provide most of the information one may need to get the "full story" behind their system, however it is far from infallable, as upgrades and servicing can muddle with results, while smaller outfits and individuals may not always be consistent in the age of their stock. As a result, it's often wise to examine the system in other ways, such as its design paradigms, considerations, features that may further give away its age.</p>
<p>It's worth noting that the kinds of things to consider when using the methods this section discusses are generally broad and vary wildly between various kinds of electronics where design considerations can differ. This section will focus primarily on small computer systems, though much of it can be extrapolated to other kinds of electronics.</p>
