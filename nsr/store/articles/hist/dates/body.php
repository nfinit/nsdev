<h1 align="center">Adding context with hardware dating</h1><hr>
<p><b>We're learning something new every day: if you have any corrections, tips 
  or more information you would like to add to improve this article, feel free 
  to mail the website administration team with your comments.</b></p>
<p>Depending on <i>what</i> and <i>why</i> you collect, it may 
  be interesting to you to further investigate when a system or component was 
  actually built. There are a few reasons, some practical and others subjective, 
  one may want to do so:</p>
<ul>
  <li>You're looking for a specific hardware revision, or a specific run of components 
    that may not be immediately identifiable otherwise</li>
  <li>You want to determine if your setup is stock, or was upgraded at some point in time</li>
  <li>In a similar vein, you want to add <i>context</i> to your system; was it 
    top-of-the-line when it was purchased, or was it made later in its production 
    lifetime, after it had been succeeded by newer models?</li>
  <li>You're intending to use the system as a prop or in some other way in which 
    the more specific timeframe than the series' general production lifetime may 
    be important</li>
  <li>You otherwise simply want to add more backstory to your system.</li>
</ul>
<p>Whichever reasons you chose (or didn't), this article presents a few simple 
  methods to obtain a more specific range of dates in which your system may have 
  been built, or even a perfectly accurate date, using design, hardware features 
  as well as the many various date codes printed on components and systems alike 
  by most chipmakers and vendors.</p>
<hr>
<h2 align="center">Direct dating</h2>
<p align="center">These are generally the most reliable methods of dating electronics, 
  using the various dates printed on the system or its components by system and 
  chip manufacturers. Depending on the format of the date, one can acertain the 
  exact age of their system, sometimes even to the time of day.</p>
<p align="center">However, there are some caveats, <b>most of these methods work 
  best with "prebuilt" OEM electronics</b>, as the components they order 
  are usually quickly put into full assemblies soon after they're recieved. This 
  can also be true of smaller outfits or home-assembled equipment, but it may 
  not be fully accurate due to factors such as components that were purchased 
  second-hand, or usage of leftover stock. If you have a "whitebox" 
  system that was built by an individual or a local computer store, it's recommended 
  to also use this in combination with other methods to estimate its age.</p>
<hr>
<h3>Date of manufacture</h3>
<p>By far the most obvious and straightforward method, some (but not all) vendors 
  will simply print the exact date at which the product left the factory, sometimes 
  even down to the specific time of day. A number of larger vendors have practiced 
  this to some capacity, although not always consistently.</p>
<p>Some of those I know to practice this are:</p>
<ul>
  <li>Apple (~1995-1998 beige PPC systems)</li>
  <li>Hewlett-Packard ("original" Vectra line up to ~1994)</li>
  <li>IBM/Lenovo (mostly on later ThinkPad systems)</li>
</ul>
<p>Other manufacturers, specifically <b>Dell</b>, may provide a date of manufacture 
  for systems online which can be accessed by providing the system's service tag 
  (in the case of Dell systems).</p>
<h3>Year and week number codes</h3>
