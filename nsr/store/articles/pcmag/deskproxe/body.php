      <h1 align="center">Compaq's Deskpro XE: Midrange for Mainstream Apps</h1>
      <p align="center"><i>By Bruce Brown, PC Magazine (<a href="https://books.google.com/books?id=VK6meblp0xsC&pg=PA48" target="_blank">8 February 1994, page 48</a>)</i></p>
      <hr>
      <p>Compaq Computer Corp.'s new midrange Deskpro XE
        line combines upgraded graphics from the Prolinea line,
        better-than-average system performance, effective audio
        utilities, and Energy Star compliance. With features,
        performance, and pricing that all fall between those of the
        Prolinea value line and the high-performing Deskpro/M systems,
        the Deskpro XE family is a sensible compromise for mainstream
        users.</p>
      <p>Deskpro XEs are available with 486SX/33, 486SX/50,
        486DX2/66, and 60-MHz Pentium processors. The unit we looked at,
        a 486DX2/66 model with 16MB of RAM, 256K external RAM cache, and
        a 340MB hard disk, is suitably equipped for mainstream business
        users. As configured, with standard software and a 3.5-inch
        floppy disk drive, the Deskpro XE 466 should sell for about
        $2,799 without a monitor. Basic models have 8MB of RAM and no
        external RAM cache.</p>
      <h2>Small Footprint</h2>
      <p>The Deskpro XE's small-foot-print 4- by 15.9- by
        15.1-inch case has limited expandability. The only extra drive
        bay is an externally accessible 5.25-inch bay for a 5.25-inch
        floppy disk, tape, or CD-ROM drive. Expansion-card accommodation
        is also limited, with just three 16-bit ISA-card slots. The 486
        models can take one half-length expansion card and two
        full-length cards, while the Pentium model can fit three
        full-length cards. None of the expansion slots have local-bus
        extensions. Four SIMM sockets permit up to 32MB of RAM on the
        486 models. The Pentium version has a maximum 136MB of RAM
        capacity.</p>
      <p>All Deskpro XE models have Compaq's enhanced
        QVision local-bus graphics integrated on the motherboard. Compaq
        didn't provide an 800-by 600-pixel resolution with 256 color
        Windows driver for this video setup, so we completed performance
        testing with a Microsoft 800-by-600 16-color driver. The
        disappointing Graphics WinMark score is likely to improve
        significantly with Compaq's own driver.</p>
      <p>On all performance tests other than the Graphics
        WinMark, the Deskpro XE 450 proved itself by scoring above
        average when compared with 486DX2/66 systems previously tested
        by PC Labs.</p>
      <p>The Deskpro XE Pentium model lacks Energy Star
        compliance, but the 486 models meet the 30-watt-or-less Energy
        Star power consumption level for both the computer and an
        attached Energy Star-compliant monitor. An easy-to-use Power
        Management software utility is standard but works only if you
        have an Energy Star-compliant monitor attached, even for setting
        the system time delay.<img src="img/deskpro-xe.GIF"
          title="Deskpro XE" alt="The XE is Energy Star-compliant"
          width="280" height="298" align="right"></p>
      <p>Compaq has upgraded its Business Audio features on
        the Deskpro XE line, including compatibility with the Microsoft
        Windows Sound System and 15:1 ratio TruSpeech audio compression.
        An included microphone either attaches to the side of your
        monitor or fits in a stand.</p>
      <p>Microsoft's Voice Pilot lets you train standard
        and custom commands to your own voice so that you can use simple
        commands to trigger the keyboard macros.</p>
      <p>Along with DOS 6.2 and Windows 3.1, Compaq's
        TabWorks is included with the Deskpro XE models. Developed by
        Xerox Corp.'s XSoft division, TabWorks uses a tabbed notebook
        metaphor for organizing and executing program, document, and
        utility icons. TabWorks is an intuitive program with a
        conventional top-line menu, left-side-of-the-screen icon-based
        ButtonStrip, tabs for different notebook pages, and a
        bottom-screen status bar. It simplifies Windows operations,
        though you pay a slight price for having yet another graphics
        application running.</p>
      <p>Since the Deskpro XE also comes with the Compaq
        Welcome Center, Deskpro users will find themselves with three
        graphical user environments: Windows, TabWorks, and the Welcome
        Center.</p>
      <p>With only modest expansion capabilities and a lack
        of local bus slots, the Compaq Deskpro XE line can only offer
        average performance compared with Compaq's high-end PC lines,
        but the XE family is a success for Compaq because it meets the
        company's goal of supplying a midrange system with good
        performance. The Deskpro XE's fine design, Business Audio, and
        Energy Star compliance make it a great system for general
        business. Compaq's three-year warranty and on-site service are
        big advantages too.</p>
      <p>List price: <i>Compaq Deskpro XE 466 with
          486DX2/66 processor, 16MB RAM, 256K external RAM cache, 340MB
          hard disk, DOS 6.2, Microsoft Windows 3.1, Compaq TabWorks,
          Windows Sound with Voice Pilot, <b>$2,799</b>.</i></p>
      <hr width="100%" size="2">
        <h2>Benchmark Tests: Compaq Deskpro XE</h2>
        <p>Except for its graphics WinMark and Winstone scores, the
          Compaq Deskpro/XE did better than the average of the 66-MHz
          systems we tested. Because Compaq did not provide a
          800-by-600, 256-color video driver, we used a Microsoft
          Windows driver for testing. With Compaq's own driver, the
          Graphics WinMark score should be higher.
        </p>
